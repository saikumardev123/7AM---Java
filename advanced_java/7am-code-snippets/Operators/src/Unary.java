
public class Unary {

	public static void main(String[] args) {
		int a=100; 
		   ++a;
		System.out.println(a);  // 101
		a++;
		System.out.println(a);  // 102
		--a;
		System.out.println(a);  // 101
		a--;
		System.out.println(a);  // 100
		a++;
		System.out.println(a);  // 101
		//logger - log
	}
}
